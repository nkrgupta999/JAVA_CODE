import java.util.*;

public class squarepattern {
    public static void main(String[] args) {
        Scanner sp = new Scanner(System.in);
        for (int i = 0; i < 4; i++){
            for(int j=0;j<4;j++){
                System.out.print("*");
            }
            System.out.println(" ");
        }
            sp.close();

    }
}
